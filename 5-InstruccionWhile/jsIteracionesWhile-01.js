/*
al presionar el botón mostrar 10 repeticiones 
con números ASCENDENTE, desde el 1 al 10.
*/

function mostrar()
{	
	//Declaro las variables
	var numero;

	//Ingreso los valores
	numero = 0;

	//Operacion while mientras que numero sea menor a 10
	while (numero < 10)
	{
		numero = numero + 1;
		console.log(numero);
	}
}//FIN DE LA FUNCIÓN

